    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
    try {  
    int linha = tabela.getSelectedRow();
    int cod = (int) tabela.getValueAt(linha, 0);
    if(controller.excluirController(cod)){
    JOptionPane.showMessageDialog(this, "EXCLUIDO COM SUCESSO", "BAIRRO", 0,
    new ImageIcon(getClass().getResource("/imagem/CERTO.png")));
    listando();
    limparCampos();
    };
    } catch (Exception e) {
    JOptionPane.showMessageDialog(this, "OPA!!!.", "CIDADE", 0,
    new ImageIcon(getClass().getResource("/imagem/OPA.png"))); 
    JOptionPane.showMessageDialog(this, "INFORME UMA LINHA NA TABELA.", "BAIRRO", 0,
    new ImageIcon(getClass().getResource("/imagem/ATENCAO.png"))); 
    }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
    if(tfId.getText().equals("")){
    JOptionPane.showMessageDialog(this, "OPA!!!.", "BAIRRO", 0,
    new ImageIcon(getClass().getResource("/imagem/OPA.png"))); 
    JOptionPane.showMessageDialog(this, "INFORME UMA LINHA NA TABELA.", "BAIRRO", 0,
    new ImageIcon(getClass().getResource("/imagem/ATENCAO.png"))); 
   
    }else{
         
    mod.setNome(tfNome.getText()); 
    mod.setBairro(tfBairro.getText());
    mod.setCidade(tfCidade.getText());
    mod.setCnpj(tfCnpj.getText());
    mod.setEmail(tfEmail.getText());
    mod.setNumero_local(tfNumero.getText());
    mod.setTelefone(tfFone.getText());

    if(controller.alterarController(mod)){
    JOptionPane.showMessageDialog(this, "Alterado com sucesso!!!.", "BAIRRO", 0,
    new ImageIcon(getClass().getResource("/imagem/CERTO.png"))); 
    listando();
    limparCampos();
    }else{
    JOptionPane.showMessageDialog(null, "Erro!!!");
    }         
    }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void tabelaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelaMouseClicked
    int linha = tabela.getSelectedRow();
    int cod = (int) tabela.getValueAt(linha, 0); 
    try {
    mod = controller.retornarController(cod);
    tfId.setText(String.valueOf(mod.getId()));
    mod.setNome(tfNome.getText()); 
    mod.setBairro(tfBairro.getText());
    mod.setCidade(tfCidade.getText());
    mod.setCnpj(tfCnpj.getText());
    mod.setEmail(tfEmail.getText());
    mod.setNumero_local(tfNumero.getText());
    mod.setTelefone(tfFone.getText());
    } catch (Exception e) {
    JOptionPane.showMessageDialog(null, "Erro!!!");
    }      
    }//GEN-LAST:event_tabelaMouseClicked

