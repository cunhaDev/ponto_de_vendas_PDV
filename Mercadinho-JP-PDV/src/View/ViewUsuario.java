    private void tabelaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelaMouseClicked
    butaocadastro.setEnabled(false);
    butaoEditar.setEnabled(true);
    butaExcluir.setEnabled(true);
    
    
    int linha = tabela.getSelectedRow();
    int cod = (int) tabela.getValueAt(linha, 0);
    try {
    modelUsuario = controllerUsuario.retornarUsuarioController(cod);
    tfId.setText(String.valueOf(modelUsuario.getId()));
    tfNome.setText(modelUsuario.getNome());
    tfSenha.setText(modelUsuario.getSenha());
    jComboBox1.setSelectedItem(modelUsuario.getTipo());
    } catch (Exception e) {
    JOptionPane.showMessageDialog(null, "Erro!!!");
    }      
    }//GEN-LAST:event_tabelaMouseClicked

