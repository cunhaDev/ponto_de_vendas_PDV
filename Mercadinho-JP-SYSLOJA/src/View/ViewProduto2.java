    private void listandoProdutos(){
        listaProduto = controllerProduto.listaModelUsuarioController();
        DefaultTableModel modelo = (DefaultTableModel) tabela.getModel();
        modelo.setNumRows(0);
        for (int i = 0; i < listaProduto.size(); i++) {
        modelo.addRow(new Object[]{
        listaProduto.get(i).getId(),
        listaProduto.get(i).getNome(),
        listaProduto.get(i).getEstoque(),
        listaProduto.get(i).getCod_barras(),
        listaProduto.get(i).getDepartamento(),
        listaProduto.get(i).getAtributo(),
        listaProduto.get(i).getInd_atributo(),
        listaProduto.get(i).getNcm(),
        listaProduto.get(i).getFornecedor(),
        listaProduto.get(i).getValidade(),
        df.format(listaProduto.get(i).getPreco_custo()),
        df.format(listaProduto.get(i).getPreco_venda())
        
        });
        }
        corLinha();
    }
