    private void butaocadastroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_butaocadastroActionPerformed
    if(tfNome.getText().equals("") || tfSenha.getText().equals("") || jComboBox1.getSelectedItem().equals("TIPO")){
    JOptionPane.showMessageDialog(this, "OPA!!!.", "USUARIO", 0,
    new ImageIcon(getClass().getResource("/imagem/OPA.png"))); 
    JOptionPane.showMessageDialog(this, "Todos os campos são obrigatórios!!!.", "USUARIO", 0,
    new ImageIcon(getClass().getResource("/imagem/ATENCAO.png"))); 
    JOptionPane.showMessageDialog(this, "NÃO IDENTIFIQUEI OS DADOS!!!.", "USUARIO", 0,
    new ImageIcon(getClass().getResource("/imagem/INFORME USUARIO.png"))); 
    }else{
    modelUsuario.setNome(tfNome.getText());
    modelUsuario.setSenha(tfSenha.getText());
    modelUsuario.setTipo(jComboBox1.getSelectedItem()+"");
    if(controllerUsuario.salvarUsuarioController(modelUsuario)>0){
      
    JOptionPane.showMessageDialog(this, "Salvo com sucesso!!!", "USUARIO", 0,
    new ImageIcon(getClass().getResource("/imagem/CERTO.png"))); 
    listando();   
    limparCampos();   
    } 
    }
    }//GEN-LAST:event_butaocadastroActionPerformed

    private void butaoEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_butaoEditarActionPerformed

    if(tfId.getText().equals("")){
    JOptionPane.showMessageDialog(this, "OPA!!!.", "USUARIO", 0,
    new ImageIcon(getClass().getResource("/imagem/OPA.png"))); 
    JOptionPane.showMessageDialog(this, "INFORME UMA LINHA NA TABELA.", "USUARIO", 0,
    new ImageIcon(getClass().getResource("/imagem/ATENCAO.png"))); 
    JOptionPane.showMessageDialog(this, "USUARIO NÃO SELECIONADO!.", "USUARIO", 0,
    new ImageIcon(getClass().getResource("/imagem/ERRO USUARIO.png"))); 
    }else{
        
    
    modelUsuario.setNome(tfNome.getText());
    modelUsuario.setSenha(tfSenha.getText());
    modelUsuario.setTipo(jComboBox1.getSelectedItem()+"");
    if(controllerUsuario.alterarUsuarioController(modelUsuario)){
      JOptionPane.showMessageDialog(this, "Alterado com sucesso!!!.", "USUARIO", 0,
    new ImageIcon(getClass().getResource("/imagem/CERTO.png"))); 
    listando();
    limparCampos();
    }else{
    JOptionPane.showMessageDialog(null, "Erro!!!");
    }         
    }
    }//GEN-LAST:event_butaoEditarActionPerformed

    private void butaExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_butaExcluirActionPerformed
    try {  
    int linha = tabela.getSelectedRow();
    int cod = (int) tabela.getValueAt(linha, 0);
    if(controllerUsuario.excluirUsuarioController(cod)){
    JOptionPane.showMessageDialog(null, "Excluído com sucesso!!!");
    listando();
    limparCampos();
    };
    } catch (Exception e) {
    JOptionPane.showMessageDialog(this, "OPA!!!.", "USUARIO", 0,
    new ImageIcon(getClass().getResource("/imagem/OPA.png"))); 
    JOptionPane.showMessageDialog(this, "INFORME UMA LINHA NA TABELA.", "USUARIO", 0,
    new ImageIcon(getClass().getResource("/imagem/ATENCAO.png"))); 
    JOptionPane.showMessageDialog(this, "USUARIO NÃO SELECIONADO!.", "USUARIO", 0,
    new ImageIcon(getClass().getResource("/imagem/ERRO USUARIO.png")));
    }
    }//GEN-LAST:event_butaExcluirActionPerformed

