    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
     if(tfCep.getText().equals("") || tfCidade.getText().equals("")){
    JOptionPane.showMessageDialog(this, "OPA!!!.", "BAIRRO", 0,
    new ImageIcon(getClass().getResource("/imagem/OPA.png"))); 
    JOptionPane.showMessageDialog(this, "Todos os campos são obrigatórios!!!.", "BAIRRO", 0,
    new ImageIcon(getClass().getResource("/imagem/ATENCAO.png"))); 
    }else{
    mod.setNome(tfCidade.getText()); 
    mod.setCep(tfCep.getText());
    if(controller.salvarController(mod)>0){
    JOptionPane.showMessageDialog(this, "Salvo com sucesso!!!.", "BAIRRO", 0,
    new ImageIcon(getClass().getResource("/imagem/CERTO.png"))); 
    listando();   
    limparCampos();   
    } 
    }
    }//GEN-LAST:event_jButton3ActionPerformed

