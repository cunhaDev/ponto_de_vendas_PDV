     private void listando(){ 
    lista = controller.listaModelController();
    DefaultTableModel modelo = (DefaultTableModel) tabela.getModel();
    modelo.setNumRows(0);
    int cont = lista.size();
    for(int i = 0; i <cont; i++){
    modelo.addRow(new Object[]{
    lista.get(i).getId(),
    lista.get(i).getNome(),
    lista.get(i).getBairro(),
    lista.get(i).getCidade(),
    lista.get(i).getCnpj(),
    lista.get(i).getEmail(),
    lista.get(i).getNumero_local(),
    lista.get(i).getRua(),
    lista.get(i).getTelefone()
    
    });
    }
    }
