    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
    if(tfNome.getText().equals("") || tfAtributo.getText().equals("") 
    || jcbDepartamento.getSelectedItem().equals("DEPARTAMENTO") || jcbFornecedor.getSelectedItem().equals("FORNECEDOR") 
    || tfBarras.getText().equals("") 
    || tfEstoque.getText().equals("")  || tfIndAtributo.getText().equals("") || tfNcm.getText().equals("") || tfValidade.getText().equals("")){
    JOptionPane.showMessageDialog(this, "OPA!!!.", "PRODUTO", 0,
    new ImageIcon(getClass().getResource("/imagem/OPA.png"))); 
    JOptionPane.showMessageDialog(this, "Todos os campos são obrigatórios!!!.", "PRODUTO", 0,
    new ImageIcon(getClass().getResource("/imagem/ATENCAO.png"))); 
    JOptionPane.showMessageDialog(this, "NÃO IDENTIFIQUEI OS DADOS!.", "PRODUTO", 0,
    new ImageIcon(getClass().getResource("/imagem/informe produto.png")));
    }else{  
        try {
            
        
    modelProduto.setNome(tfNome.getText());
    modelProduto.setAtributo(tfAtributo.getText());
    modelProduto.setCod_barras(tfBarras.getText());
    modelProduto.setDepartamento(jcbDepartamento.getSelectedItem()+"");
    modelProduto.setEstoque(Integer.parseInt(tfEstoque.getText()));
    modelProduto.setFornecedor(jcbFornecedor.getSelectedItem()+"");
    modelProduto.setInd_atributo(tfIndAtributo.getText());
    modelProduto.setNcm(tfNcm.getText());
    modelProduto.setValidade(tfValidade.getText()); 
    
    modelProduto.setPreco_venda(Double.parseDouble(tfVenda.getText()));
    modelProduto.setPreco_custo(Double.parseDouble(tfCusto.getText()));
    modelProduto.setValidade(tfValidade.getText()); 
    if(controllerProduto.salvarController(modelProduto)>0){
    JOptionPane.showMessageDialog(this, "Salvo com sucesso!!!.", "PRODUTO", 0,
    new ImageIcon(getClass().getResource("/imagem/CERTO.png"))); 
    limpandoCampos();
    listandoProdutos();
    tfNome.requestFocus();
    }
    } catch (Exception e) {
       JOptionPane.showMessageDialog(this, "As caixas de textos que que contém (*) só pode informar números."); 
        }
    }
    
    }//GEN-LAST:event_btnCadastrarActionPerformed

