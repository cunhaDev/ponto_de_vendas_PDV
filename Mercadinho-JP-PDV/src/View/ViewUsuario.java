    private void listando(){
    listaModelUsuario = controllerUsuario.listaModelUsuarioController();
    DefaultTableModel modelo = (DefaultTableModel) tabela.getModel();
    modelo.setNumRows(0);
    int cont = listaModelUsuario.size();
    for(int i = 0 ; i < cont; i++){
    modelo.addRow(new Object[]{
    listaModelUsuario.get(i).getId(),
    listaModelUsuario.get(i).getNome(),
    listaModelUsuario.get(i).getSenha(), 
    listaModelUsuario.get(i).getTipo()
    });
    }
    }
